export const getCertificationColor = (cert: string): string => {
  const colorMap: Record<string, string> = {
    "Fair Trade": "bg-forest/20 text-forest",
    "Fairtrade": "bg-forest/20 text-forest",
    "USDA Organic": "bg-green-100 text-green-800",
    "Organic": "bg-green-100 text-green-800",
    "India Organic": "bg-green-100 text-green-800",
    "Rainforest Alliance": "bg-emerald-100 text-emerald-800",
    "UTZ Certified": "bg-blue-100 text-blue-800",
    "Non-GMO": "bg-amber-100 text-amber-800",
    "FSSAI": "bg-orange-100 text-orange-800",
    "ISO 22000": "bg-slate-100 text-slate-800",
    "Vegan": "bg-lime-100 text-lime-800",
    "Kosher": "bg-purple-100 text-purple-800",
    "B Corp": "bg-teal-100 text-teal-800",
  };
  
  for (const key of Object.keys(colorMap)) {
    if (cert.toLowerCase().includes(key.toLowerCase())) {
      return colorMap[key];
    }
  }
  return "bg-secondary text-secondary-foreground";
};
